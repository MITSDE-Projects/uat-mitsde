                        <h3>How Does Human Capital Management Work?</h3>
                        <p>
                            At its heart, it’s about linking the right systems and practices to support your people. Let’s break it down into 5 key stages:
                        </p>

                        <ul>
                            <li><strong>Workforce Planning –</strong> Define who you need, when, and for what roles.</li>
                            <li><strong>Talent Acquisition –</strong> Utilise structured interviews and assessments to select the most suitable candidates.</li>
                            <li><strong>Onboarding & Culture Fit –</strong> Get new employees up to speed quickly. Focus on job clarity and team values.</li>
                            <li><strong>Learning & Growth –</strong> Track the skills people possess and identify what they need next.</li>
                            <li><strong>Performance Monitoring –</strong> Use simple data to track progress regularly, not just once a year.</li>
                        </ul>

                        <p>
                            This framework applies to both large corporations and growing small businesses. At MIT SDE, students studying HR and management subjects gain hands-on experience with these principles, preparing them for real-world roles in this field.
                        </p>

                        <h3>So, What’s Next for Human Capital Management?</h3>
                        <p>
                            Technology is changing how people work. So, the future of human capital management will likely include:
                        </p>
                        <ul>
                            <li>Using predictive data to understand staff turnover before it happens</li>
                            <li>Tracking soft skills like creativity and adaptability</li>
                            <li>Matching employee goals with organisational missions</li>
                            <li>Moving away from hierarchy to project-based teamwork</li>
                        </ul>

