                                students securing a job.
                            </li>
                            <li>
                                <b>Highest Salary -</b> This is a straightforward metric that shows the highest salary
                                paid to a student recruited via campus placement.
                            </li>
                            <li>
                                <b>Average Salary -</b> The average <b>PGDM salary</b> is the mean amount of wages. A
                                higher average salary indicates that students are securing lucrative <b>PGDM jobs</b>
                                via campus placement.
                            </li>
                            <li>
                                <b>Median Salary -</b> This metric indicates the midpoint salary offered to college
                                students, considering both the lowest and highest paying salaries secured through
                                college campus placement.
                            </li>
                            <li>
                                <b>Top Recruiters -</b> Top recruiters are those companies that hire the most number of
                                students via college placement drives and programs.
