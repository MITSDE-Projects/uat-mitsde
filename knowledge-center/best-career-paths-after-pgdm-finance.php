                <h2>Core Skills Gained Through a PGDM in Finance</h2>
                <p>A PGDM Finance course is not just about learning how to balance the books. It’s a robust programme
                    that equips students with a mix of analytical, strategic, and managerial skills essential for
                    today’s finance professionals.</p>
            </div>
            <div class="row">
                <div class="col-md-12 col-lg-12 col-xl-12">
                    <div class="alumini-box content">
                        <ol>
                            <li>
                                <strong>Financial Analysis and Modelling:</strong> Students learn to interpret financial
                                statements, forecast financial performance, and build financial models. These are
                                critical skills for roles in investment banking, equity research, and corporate
                                strategy.
                            </li>
                            <li>
                                <strong>Investment and Portfolio Management:</strong> Understanding the art and science
                                behind selecting the right investment avenues is key. Students study asset classes,
                                risk-return trade-offs, market dynamics, and investment strategies.
                            </li>
                            <li>
                                <strong>Risk Management:</strong> In a volatile market, risk management is everything.
                                Students are trained to identify, assess, and mitigate financial risks. This includes
                                credit risk, operational risk, market risk, and even geopolitical risk.
                            </li>
                            <li>
                                <strong>Strategic Planning and Budgeting:</strong> Budgeting isn’t just about expense
                                tracking. It’s about aligning financial goals with organisational strategy. PGDM
                                graduates learn to plan long-term budgets, assess capital allocation, and align
                                resources to maximise ROI.
                            </li>
                            <li>
                                <strong>Regulatory and Compliance Knowledge:</strong> With increasing regulation across
                                industries, understanding financial laws, accounting standards, and regulatory bodies
                                (like RBI, SEBI) is crucial. This knowledge helps graduates ensure that their
                                organisations stay on the right side of the law.
                            </li>
                        </ol>
