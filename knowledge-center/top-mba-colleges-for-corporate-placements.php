              <li><strong>Make the most out of Live Projects & Internships:</strong> Company-sponsored projects and internships are the windows of opportunity for any MBA student. If they treat it seriously and perform well, they even stand a chance to get a pre-placement offer by the company.</li>
              <li><strong>Engage in Case Competitions & Corporate Events:</strong> Company-sponsored case study competitions, hackathons, and business plan contests give students a chance to display their talent directly to the recruiters. Excellent performance in such events helps the deserving become a top candidate during placements.</li>
              <li><strong>Enhance communication and soft skills:</strong> Students need to focus on improving the group discussion tactics, presentation skills, and interview readiness with the help of mock sessions. Recruiting companies always value those candidates who know how to clearly present new ideas and collaborate effectively.</li>
              <li><strong>Network with Industry Leaders & Alumni:</strong> Focus on building meaningful connections with the alumni through industry summits, alumni meets, and LinkedIn. They often hold influence over hiring decisions. Moreover, they can provide students with valuable insider tips, too. Thus, it boosts the chances of placement significantly.</li>
              <li><strong>Customise profiles and resumes:</strong> Never apply with a generic resume. Customise the CV by highlighting certifications and achievements specific to the roles. Students also need to update their profiles on LinkedIn regularly.</li>
              <li><strong>Upskill continuously:</strong> Add an extra edge to your profile with relevant certifications in supply chain tools, financial modelling, digital marketing, and data analytics. Recruiters value candidates who know how to combine industry-ready skills with academic knowledge to achieve company goals.</li>
