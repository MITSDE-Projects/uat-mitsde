                <h2>Application Process</h2>
            </div>
            <div class="row">
                <div class="col-md-12">
                    <div class="alumini-box content">
                        <p>The MBA application form process for Pune colleges depends on the college and the entrance exam you've taken:</p>
                        <h5>Centralized Admission Process (CAP)</h5>
                        <p>If you have appeared for the MAH MBA CET, you must enroll through the Centralized Admission Process (CAP) managed by the Directorate of Technical Education (DTE) in Maharashtra. You’re required to:</p>
                        <ul>
                            <li>Register online</li>
                            <li>Verify and upload your documents</li>
                            <li>Fill in your college preferences</li>
                        </ul>
                        <p>Seats are allotted based on your rank, category, and preferences. The CAP is conducted in multiple rounds to give every candidate a fair chance to secure a seat.</p>
                        <h5>Direct Application to Institutes</h5>
                        <p>If you want to go to a college that accepts scores from tests like the CAT or SNAP, you usually have to apply directly to each one separately. Just like CAP, you’re required to apply through the official website, filling out an application form.</p>
                        <p>Be prepared to pay a non-refundable application fee for each college you apply to. These fees can range from a few hundred to a couple of thousand rupees. The application windows are usually open from August to January.</p>
                    </div>
                </div>
            </div>
        </div>

    </section>

    <section class="accreditation">
        <div class="container">
            <div class="sec-head">
                <h2>Selection Process</h2>
            </div>
            <div class="row">
                <div class="col-md-12">
                    <div class="alumini-box content">
                        <p>The final selection process looks at more than just your entrance exam score; it looks at your whole profile. Most of the best business schools use a multi-step process to see if you are a good fit for their program.</p>
                        <p>The most common parts of the selection process are:</p>
                        <ul>
                            <li><strong>Group Discussion (GD):</strong> A GD tests your ability to communicate, lead, work as part of a team, and think critically about a topic. The panel gives a topic to test how well you respect other people's opinions and contribute to the group's discussion.</li>
                            <li><strong>Written Ability Test (WAT):</strong> Many colleges use WAT to check your critical thinking skills. You will be given a topic and a time limit to write a short essay that shows how clearly you can think and how well you can make an argument.</li>
                            <li><strong>Personal Interview (PI):</strong> It’s conducted to know your personality and why you want to pursue an MBA. You can expect questions on your academic background, work history, strengths and weaknesses, and reasons for choosing the specific college.</li>
                        </ul>
                        <p>Above all these, the panel also assesses your grades, work experience, and overall profile.</p>
                    </div>
                </div>
            </div>
