            <h4 class="bold mgbt-17">Distance MBA in Nagaland</h4>
        <p align="justify">Also known as “The Land of Festivals”, Nagaland is one of the seven sister states in eastern India. It is covered by mountains from all sides. It is less densely populated and thus the smallest state of the country. Most of the state’s population stays in the rural area but approximately 80% of them are literate and speak English. However, the state lacks in terms of educational facilities. There are only a few colleges in Nagaland. That is why the students are forced to move to other states to complete their higher studies. In such a scenario, MAEER MIT Pune’s MIT School of Distance Education (MIT-SDE) serves as a medium to give higher education to all the students of the state. MIT-SDE fulfills the dreams of the students by offering MBA-equivalent courses via distance learning mode. So, the students can attain professional management diploma just by sitting at their home according to their schedule. The courses even come as a ray of hope for the working professionals who wish to fast-track their stagnant career.</p>

     
