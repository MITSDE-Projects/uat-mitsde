            <p align="justify">Since the state is quite small in terms of area and most of its area is covered with dense forest, there are quite a few industries operating in the state mainly different cottage industries and tourism industries. The main source of income of people is agriculture which has contributed mainly to the state’s GDP other than the tourism industry. As there are many literate people in the state, so higher education can help people to earn a decent living in the state. MIT-SDE equips the students with entrepreneurship skills which would indeed help them in their future businesses. Moreover, if you are planning to work in the industries, studying from MIT-SDE can help you to secure a decent job in the organizations. MIT-SDE equips the candidates with value-added skills and makes them job ready so that they are immediately absorbed in the industry.</p>
             
             
             
              
              
