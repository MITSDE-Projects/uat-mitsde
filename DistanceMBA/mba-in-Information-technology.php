            <h4 class="bold mgbt-17">MBA in Information Technology</h4>
            <p align="justify">With the advent of technology, the field of Information Technology is on a rapid rise. Today, IT is an integral part of every organization. That is why organizations have an IT team headed by IT managers. IT team takes care of IT-related problems of an organization. There are different responsibilities that need to be performed by IT managers. So, he/she must have the know-how of the field in order to ensure proper IT management. In such a scenario, an MBA in Information Technology is one such course that educates the candidates of technological as well as managerial aspects pertaining to the field and prepares them to enter the field as proficient IT manger.</p>
              
            



            <h4 class="bold mgbt-17">Duties performed by Information Technology Professionals </h4>
            <p align="justify">Computers and cutting-edge technologies were introduced to facilitate seamless and smoother functioning of day-to-day operations in organizations. In this regard, proper functioning of the system along with their maintenance and troubleshooting is required. Some of the duties to be performed by IT managers are:  </p>
             <div class="clearfix"></div>
            
            
             <h4 class="bold mgbt-17">Staff Supervision </h4>
             <p align="justify">IT managers are expected to be involved only in computers, but they have more duties to be performed. They act as staff supervisors.  When new employees are hired in an organization, they need to be trained and made efficient to handle IT-related projects successfully. Thus, IT managers act as a staff supervisor. They conduct training & development of employees as well as take care of performance management. </p>


             <h4 class="bold mgbt-17">IT Budget Monitoring </h4>
             <p align="justify">It is the responsibility of the IT manager to prepare the annual budget of his department and oversee it on a regular basis. Monitoring the IT budget involves regular reviewing of contracts, controlling the expenses and cost approvals. In some cases, the IT manager and the upper- level executives may fix a meeting for the approval of the budget.</p>


             <h4 class="bold mgbt-17">Software Development </h4>
             <p align="justify">The IT manager must keep the computer's hardware and software up-to-date and integrate them with cutting-edge technologies. Further, they must be successful in product evaluation and keep personal contact with the vendors for setting up different demonstrations. </p>
			

             <h4 class="bold mgbt-17">Systems Management  </h4>
             <p align="justify">One of the basic duty performed by an IT professional is to ensure system management. Systems management is concerned with the computer system's monitoring which includes software, infrastructure as well as networks. </p>
			 

