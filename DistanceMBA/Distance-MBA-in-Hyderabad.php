     <ul class="flat-list">
     <li>The self-attested scanned copies of the 10th standard mark sheet or certificate from a recognized board</li>

     <li>12th-grade pass mark sheet/equivalent course mark sheet</li>

     <li>Graduation mark sheet/certificate from a recognized university</li>

     <li>Passport size color photograph & payment receipt</li>
      </ul>


        

 <br><p align="justify"><b>2. Full Support to Student</b></p>

<p align="justify">MIT-SDE offers career counseling for every student before and after completing the PGDM course. These student counselors help students during the process of admission, submitting the needed documents, and making payments. Students' counselors also help students to find information regarding the teaching staff, courses, syllabus, etc</p>



<p align="justify"><b>3. Connecting with students efficiently</b></p>

<p align="justify">MITSDE stays connected with students by regularly updating their website and sending  SMS to the students on their mobile numbers. For full support, the skilled faculty also sends emails on job openings, regularly asks interview questions, provides interview tips, market trends, etc. to the registered Email IDs of the students.</p>



<p align="justify"><b>4. Types of Courses in MIT-SDE</b></p>

<p align="justify">MIT-SDE offers three courses in MBA-equivalent distance programs. These courses are a Post-graduate Certificate in Management, Post Graduate Diploma, and PGDBA. </p>

            

<p align="justify"><b>5. Duration of courses</b></p>

<p align="justify">PGDM/ PGDBA courses in distance from MIT-SDE continue for 18 Months to 24 Months. Post Graduate Certificate in Management is a short 1-Year course. The Post Graduate Diploma in Management is a one and a half years course. Finally, PG Diploma in Business Administration is a 2-years course. </p>



