             <p align="justify">Today, marketing is prevailing as the most interesting and popular field among the aspirants. Since every product and service needs effective marketing to reach to its target customers, so the field of marketing is laden with innumerable opportunities. A PGDBA in Marketing Management is the course that trains marketing professionals. It makes candidates familiar with different forms of marketing such as advertisement & sales promotion, international marketing, industrial marketing, product & brand management and financial services. </p>
			 
			
