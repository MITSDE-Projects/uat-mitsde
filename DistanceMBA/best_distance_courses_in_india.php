 <p align="justify">A part of the reputed MIT Pune, MIT-SDE is a pioneer distance learning institute of India. It offers MBA-equivalent courses in various specialisations of management. Currently, it offers PGDM, PGCM and PGDBA in various exciting specialisations of management.</p>  


              <h4 class="bold mgbt-17">Post Graduate Certificate in Management (PGCM)</h4>
 <p align="justify">PGCM is a one-year program offered by MIT-SDE. It is an entry-level programme that trains the candidates to enter the corporate world. The course familiarises the candidates with fundamentals and concepts of the business world. The candidates can choose from specialising in Modern Office and Retail Management. This course focuses on enhancing the management skills of the candidates that are essential in this challenging and developing economy. Since the business environment is ever-changing, PGCM equips you with industry-specific knowledge and allows you to learn while earning. </p> 


              <h4 class="bold mgbt-17">Post Graduate Diploma in Management (PGDM)</h4>
 <p align="justify">AICTE approved PGDM course is an 18-months program offered by MIT-SDE. This course enhances managerial skills as well as the entrepreneurial skills of candidates. Candidates can choose from various specialisations such as IT, Finance Management, Infrastructure, Material, Operations, Marketing Management and many more. Diploma in these specialisations gives one access to enhanced professional opportunities and a wide platform for shaping career.  </p>   
 
 
              <h4 class="bold mgbt-17">Post Graduate Diploma in Business Administration (PGDBA)</h4>
 <p align="justify">A PGDBA course is similar to distance MBA course. The duration of the course is 2 years. This course teaches industry-relevant skills to the candidates and hones their business acumen. There is an option for the candidates to choose any specialisation from Operations, Material Management, IT, HR, Marketing, Finance and Supply Chain. This course prepares candidates to face upcoming business challenges. It keeps them abreast of trends in the industry and prepares them to get immediately absorbed in the industry. </p> 
