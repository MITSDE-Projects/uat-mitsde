            <p align="justify">The second-largest and the most populous city of Vidarbha region in Maharashtra is Amravati. Amravati is the variant version of the original name of ‘Audumbaravati’. Amravati is one of the oldest cities in the country; the existence of this city can be dated as back as 1097. Amaravati has gotten its name from The Amba Devi Temple and is a tourist place where most of the people visit whenever they are in Amravati. The temple has beautiful construction which one should not miss. The other attraction where the tourist can go to is the Melghat Tiger Reserve; there are some museums and waterfall which are worth the visit.</p>


