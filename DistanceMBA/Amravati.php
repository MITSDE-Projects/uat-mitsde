            <p align="justify">The industries in Amravati mainly comprises of manufacturing industries, the significant chunk of manufacturing units produces food products and beverages followed by manufacturing of wearing apparel, dyeing, to name a few industries. A growth of trend has been observed in various industries like Auto repairing, general engineering, agro-based industries, textile industries and non-metallic mineral industries, to name a few. The growing trend of these industries has given rise to labour intensive workload. The parents of the students in Amravati would like their child to see in a white collared executive job. With the ever-increasing competition, the student has to have something additional in order to stand out from the rest. MIT School of Distance Education will help the students to get a degree which would enhance their skillset and knowledge.</p>   
			
			
