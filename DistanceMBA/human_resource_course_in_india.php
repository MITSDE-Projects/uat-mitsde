           <h1 class="font-16 text-theme-colored2">Human Resource Course in India:</h1>
            <p align="justify">Human Resource Management is an integral part of every organisation. It is concerned with managing the workforce so that they become an asset for the company and let the business gain a competitive advantage over others. The task of HR managers includes posting job openings, selecting & hiring candidates, training the selected candidates, motivating the employees and looking after their concerns. HR management is important for an organisation for the following reasons: </p>
			  <p align="justify">1.	Saves extra expenditure in the hiring process: In the candidates’ selection process, different tests need to be performed, which adds to a company’s expenses. However, Human Resource professionals know different ways which can reduce the cost incurred during the process of hiring.</p>
			  <p align="justify">2.	Reduces Employee Turnover: It is the duty of HR managers to provide better working conditions to the employees which, in turn, reduces employee turnover. They regularly consult the employees about their issues, motivate them and look for their growth within the organisation.</p>
			  <p align="justify">3.	Training and Development: Training of employees is an important step as it facilitates them to understand the working of organisation. It is the task of human resource managers to create effective training and development programs so that candidates can acquire maximum skills and bring benefit to the company. </p>
			  <p align="justify">4.	Conflict Resolution: Every individual appointed in a company, has a different personality and attitude. There might be situations where conflicts may arise between two or more individuals within the company. In such a situation, HR managers settle the dispute by equally assisting both the parties. </p>
