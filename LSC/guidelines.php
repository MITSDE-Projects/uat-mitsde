<li>Ability to relevantly classify and categorise information which is being rolled out and thereby to find connections, relationships to draw rational inferences("pattern seeking")</li>
<li>Ability to "learn" as opposed to bookish knowledge. Capacity for learnability by being mentally agile and flexible (learning to learn)</li>
<li>From the above, capacity to generate knowledge and its application</li>
<li>Ability to handle different materials and media which may or may not be familiar</li>
<li>Ability to emphatise - being sensitive towards feelings, needs and emotions of others and to have an ability to relate oneself with others in various contexts, specific situations, events etc.</li>
<li>Having unbounded curiosity towards things, people, situations etc,. A sense of wonderment, having a constant urge to ask questions and an urge to find out the what's, whys, how's, when's about the world and beyond.</li>
<li>Being individualistic i.e., having independent viewpoints and convictions (as opposed to stubbornness)</li>
<li>Being very much aware about what is going on in the world and beyond and having a "worldview"</li>
<li>Good ability to communicate - both verbal communication and non-verbal communication(may be through drawing, sketching and other media)</li>
<li>Ability to function or work as a good team member</li>
<li>Having an ability to be tenacious - not "giving up" easily</li>
<li>Attitude towards neatness, precision and production of quality work</li>
