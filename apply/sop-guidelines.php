	With Avantika University, each and every applicant is required to submit a Statement of Purpose. It is your opportunity to help reviewers assess better your understanding of Avantika's academic objectives to determine whether you fit the criteria for the program you are applying. 
                </p>		
				<br>
                <p>
                    <b>What is Statement of Purpose (SoP)?</b><br>
                    The SoP is one of the most important components of your application process. The SoP will allow the admissions committee to get acquainted with you, your desire to study at Avantika University, and future plans to utilize your learning. The SoP should also include your reasons for undertaking the choice of your program at Avantika University. It should include an explanation of your academic interests, including their relation to your goals for undergraduate/postgraduate study. 
                    <br>
								</p>
								
								<b>How to write the SoP?</b><br>
								 A Statement of Purpose requires a deeper thought, a deep investment of time, and careful consideration. You need to put your best foot forward. It should be drafted with the orientation to convince, assure, and update the faculty members from the selection committee about your substantial achievements that demonstrate your success to take on the programs at undergraduate or postgraduate level. Your statement should comprise certain elements for better consideration as listed here: 
                </p>
                
                  <ul><li>  Be Yourself: Be Specific, Concise, Honest, and Unique.
<li>Begin with a Strong Opening: Write an opening that will grab the admission committee panel’s attention. Your objective should be to stand out from the multitude of other applicants. 
<li>The right framing of points will ensure that you make a positive impression on the selection committee. 
<li>  Highlight an extraordinary experience that is relevant to the program of your interest. Ideally, you must include this at the beginning of your SoP document. The objective must be to highlight how this has contributed to making you the person you are today. 
<li>  Talk about your career goals beyond this program, and how it will help you accomplish your aims. 
<li>  Briefly talk about shortcomings from your education journey. 
<li>Avoid being too precise and mindful of your personal details. 
</ul>
										
                <p>
                    <b>Length and Format: </b>
                    <br>
                 It should include your full name and the proposed field of study at the top of each page. 
                    <ol>
                        <li>Word Count: 500 words</li>
                        <li>Format:
                        <ul>
                                <li>Spacing: Single line spacing 
<li>Margins: One-inch Margins 
<li>Font size: Arial font size 10 for ease of reading (preferred) 
                        </ul></li>
                    </ol>
                </p>
                <p>
                </p>
                <p>
									<b>Things to Avoid When Writing a Statement of Purpose:  </b>
                    <ul>
                        <li>Errors, misspellings, poor English 
<li>Plagiarism: Copying and submitting another student’s Statement of Purpose (SoP) is prohibited.
<li> Being too personal in your essay, and sharing deep personal problems or excuses for past performances. 
<li> Being repetitive or vague in your SoP document 
<li> Criticize programs from other institutions/universities 
<li>Using uncommon/ jargoned words that look like they came from a thesaurus. 
<li>Elaborating on your entire life story to the committee. 
<li>Submit false or irrelevant information in your essay. 

                    </ul>
                </p>
				<br>		
					
                                            <b>Essential Tips:</b>

                    <ul>
                        <li>The admissions committee will assess your potential on self-motivation and competence.
<li>Write with an active stance and emphasize everything from a positive perspective. 
<li>Demonstrate perseverance despite obstacles if they affected your grades. The objective should be to elaborate your perseverance in overcoming obstacles. 
<li>Highlight your education approach from an early age and link it with continuity and focus. 
<li>Be concise unless specified, as the desired outcome of this exercise is to express yourself with brevity.
<li> Your tone of the SoP should reflect what is special, diverse, unique, or impressive about you. 
<li>Additionally, the final draft must contain appropriate sentence variations and should be free of spelling and grammatical errors. 
<li>Be sure to maintain a backup of the MS Word file of your SoP document for future reference. 

                    </ul>
