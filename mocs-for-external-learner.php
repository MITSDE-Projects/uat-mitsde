        document.addEventListener('DOMContentLoaded', function () {
            const carousel = document.getElementById('carouselCustom');
            const slides = Array.from(carousel.querySelectorAll('.slide'));
            const nextBtn = carousel.querySelector('.next-btn');
            const prevBtn = carousel.querySelector('.prev-btn');
            let activeIndex = 0;
            const n = slides.length;
            let autoplayInterval = null;
            const autoplaySpeed = 3000; // ms

            function update() {
                slides.forEach((s, i) => {
                    s.classList.remove('pos-active', 'pos-prev', 'pos-next', 'hidden');
                    if (i === activeIndex) s.classList.add('pos-active');
                    else if (i === (activeIndex - 1 + n) % n) s.classList.add('pos-prev');
                    else if (i === (activeIndex + 1) % n) s.classList.add('pos-next');
                    else s.classList.add('hidden');
                });
            }

            function nextSlide() {
                activeIndex = (activeIndex + 1) % n;
                update();
            }

            function prevSlide() {
                activeIndex = (activeIndex - 1 + n) % n;
                update();
            }

            function startAutoplay() {
                autoplayInterval = setInterval(nextSlide, autoplaySpeed);
            }

            function stopAutoplay() {
                clearInterval(autoplayInterval);
                autoplayInterval = null;
            }

            // Init
            update();
            startAutoplay();

            // Button handlers
            nextBtn.addEventListener('click', () => {
                nextSlide();
                stopAutoplay();
                startAutoplay();
            });

            prevBtn.addEventListener('click', () => {
                prevSlide();
                stopAutoplay();
                startAutoplay();
            });

            // Keyboard support
            document.addEventListener('keydown', (ev) => {
                if (ev.key === 'ArrowRight') { nextSlide(); stopAutoplay(); startAutoplay(); }
                else if (ev.key === 'ArrowLeft') { prevSlide(); stopAutoplay(); startAutoplay(); }
            });

            // Swipe support
            let startX = null;
            carousel.addEventListener('touchstart', (e) => { startX = e.touches[0].clientX; });
            carousel.addEventListener('touchend', (e) => {
                if (startX === null) return;
                const diff = e.changedTouches[0].clientX - startX;
                if (Math.abs(diff) > 30) {
                    if (diff < 0) nextSlide();
                    else prevSlide();
                    stopAutoplay();
                    startAutoplay();
                }
                startX = null;
            });
        });
    </script>


