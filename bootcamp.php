            document.addEventListener("DOMContentLoaded", function () {
                const links = document.querySelectorAll(".app-features a");
                const sections = ["overview", "trackOffered", "roadMap", "careerOutcomes", "learnExpert", "bootcampSchedule", "successStories"];

                window.addEventListener("scroll", function () {
                    let current = "";
                    const navbarHeight = 150;
                    sections.forEach(id => {
                        const el = document.getElementById(id);
                        if (el) {
                            const rect = el.getBoundingClientRect();
                            if (rect.top <= navbarHeight && rect.bottom > navbarHeight) {
                                current = id;
                            }
                        }
                    });
                    if (!current) {
                        sections.forEach(id => {
                            const el = document.getElementById(id);
                            if (el && window.scrollY >= el.offsetTop - navbarHeight) {
                                current = id;
                            }
                        });
                    }
                    links.forEach(link => {
                        link.classList.remove("active");
                        if (link.getAttribute("href") === "#" + current) {
                            link.classList.add("active");
                        }
                    });
                });

                links.forEach(link => {
                    link.addEventListener("click", function (e) {
                        e.preventDefault();
                        const target = document.querySelector(this.getAttribute("href"));
                        if (target) {
                            window.scrollTo({ top: target.offsetTop - 120, behavior: "smooth" });
                        }
                    });
                });
            });
