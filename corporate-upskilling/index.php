                $('#prevBtn').prop('disabled', page === 0);
                $('#nextBtn').prop('disabled', page === totalPages - 1);
            }

            $('#prevBtn').click(function () {
                if (currentPage > 0) {
                    currentPage--;
                    showPage(currentPage);
                }
            });

            $('#nextBtn').click(function () {
                if (currentPage < totalPages - 1) {
                    currentPage++;
                    showPage(currentPage);
                }
            });

            // Initial show
            $cards.hide(); // Hide all initially
            showPage(currentPage);
        });
    </script>

