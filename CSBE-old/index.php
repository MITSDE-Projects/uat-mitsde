        $(document).ready(function () {
            const $cards = $('.service-card');
            const cardsPerPage = 3;
            let currentPage = 0;
            const totalPages = Math.ceil($cards.length / cardsPerPage);

            function showPage(page) {
                $cards.each(function (index) {
                    if (index >= page * cardsPerPage && index < (page + 1) * cardsPerPage) {
                        $(this).stop(true, true).fadeIn(400); // Smoothly show cards
                    } else {
                        $(this).stop(true, true).fadeOut(400); // Smoothly hide cards
                    }
                });

                $('#prevBtn').prop('disabled', page === 0);
                $('#nextBtn').prop('disabled', page === totalPages - 1);
            }

            $('#prevBtn').click(function () {
                if (currentPage > 0) {
                    currentPage--;
                    showPage(currentPage);
                }
            });

            $('#nextBtn').click(function () {
                if (currentPage < totalPages - 1) {
                    currentPage++;
                    showPage(currentPage);
                }
            });

            // Initial show
            $cards.hide(); // Hide all initially
            showPage(currentPage);
        });
    </script>

