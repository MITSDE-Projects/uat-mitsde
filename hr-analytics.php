                <p>
                    HR analytics is using data to understand how people work inside a company. It helps Human Resource (HR)
                    teams make better decisions about hiring, training, employee wellbeing, and performance. Instead of relying
                    on guesswork, HR professionals base their actions on actual data.
                </p>

